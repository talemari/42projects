#include "mymlx.h"
#include "math.h"

t_mat4x4f		mm_mat_scale(t_vec3f vec)
{
	t_mat4x4f	res;

	res.v[0][0] = vec.x;
	res.v[0][1] = 0;
	res.v[0][2] = 0;
	res.v[0][3] = 0;
	res.v[1][0] = 0;
	res.v[1][1] = vec.y;
	res.v[1][2] = 0;
	res.v[1][3] = 0;
	res.v[2][0] = 0;
	res.v[2][1] = 0;
	res.v[2][2] = vec.z;
	res.v[2][3] = 0;
	res.v[3][0] = 0;
	res.v[3][1] = 0;
	res.v[3][2] = 0;
	res.v[3][3] = 1;
	return (res);
}
