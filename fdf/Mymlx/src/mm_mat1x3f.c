#include "mymlx.h"

t_mat1x3f		mat1x3f(t_vec3f vec)
{
	t_mat1x3f		res;

	res.c[0] = vec.x;
	res.c[1] = vec.y;
	res.c[2] = vec.z;
	return (res);
}
